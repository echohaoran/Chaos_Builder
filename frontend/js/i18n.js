function detectLocale() {
  try {
    const saved = localStorage.getItem(I18N_KEY);
    if (saved && SUPPORTED_LOCALES.includes(saved)) return saved;
  } catch (_) {}
  return DEFAULT_LOCALE;
}

let currentLocale = detectLocale();

function t(key, fallback, vars) {
  let value = DICTIONARY[currentLocale]?.[key] ?? fallback ?? key;
  if (vars) {
    Object.keys(vars).forEach(function (k) {
      value = value.replace(new RegExp(`\\{${k}\\}`, 'g'), vars[k]);
    });
  }
  return value;
}

function setLocale(locale) {
  if (!SUPPORTED_LOCALES.includes(locale)) return;
  currentLocale = locale;
  try {
    localStorage.setItem(I18N_KEY, locale);
  } catch (_) {}
  document.documentElement.lang = locale;
  applyTranslations();
  document.dispatchEvent(new CustomEvent('i18n:changed', { detail: { locale } }));
}

function translateElement(el) {
  const key = el.getAttribute('data-i18n');
  const htmlKey = el.getAttribute('data-i18n-html');
  const attr = el.getAttribute('data-i18n-attr');

  if (htmlKey) {
    const value = t(htmlKey);
    if (value !== htmlKey) el.innerHTML = value;
  } else if (key) {
    const value = t(key);
    if (value !== key) el.textContent = value;
  }

  if (attr) {
    const attrKey = el.getAttribute(`data-i18n-${attr}`);
    if (attrKey) {
      const value = t(attrKey);
      if (value !== attrKey) el.setAttribute(attr, value);
    }
  }
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n], [data-i18n-html], [data-i18n-attr]').forEach(translateElement);
}

function toggleLocale() {
  setLocale(currentLocale === 'zh-CN' ? 'en' : 'zh-CN');
}

function initI18n() {
  document.documentElement.lang = currentLocale;
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', applyTranslations);
  } else {
    applyTranslations();
  }
}

window.I18N = {
  locale: () => currentLocale,
  set: setLocale,
  toggle: toggleLocale,
  t,
  apply: applyTranslations,
  init: initI18n,
  supported: SUPPORTED_LOCALES,
};

initI18n();
